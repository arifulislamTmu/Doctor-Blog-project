                <h3  style="  color: #1B405A;">Application of PRP Therapy for Injuries</h3>
                <p class="text_description" style="  color: #1B405A;">
                <h5>  What is PRP?</h5>
                    Platelet-rich plasma (PRP) involves taking a small sample of blood and spinning it in a
                    centrifuge to concentrate and separate the platelets and plasma from the other components
                    (red blood cells) of your blood.
                    This concentrated solution is then injected back into the body at the injury site.
                </p>

   
                <p class="text_description" style="color: #1B405A;">
                  <h5> WHY does it work?</h5>
                  <img src="info/PRP knee.jpg" alt="" class="img-fluid">
                    Platelets are the part of the blood that contain growth factors and proteins. 
                    These are very important in the repair and regeneration of damaged tissue. PRP
                    Therapy works by accelerating the body’s own healing process, by adding a
                    concentrated solution of these healing growth factors to the damaged tissue. 
                    They address the injury site by encouraging the growth of new, healthy cells.
                  </p>


                  <p class="text_description" style="  color: #1B405A;">
                    <h5> What injuries can PRP be used for?</h5>
                    PRP can be used to effectively treat many ailments or injuries. 
                    Weakened ligaments and tendons, cause the majority of the pain 
                    and damage related to joints. But many patients with degenerative or arthritic joints
                     have also had significant relief with a course of PRP therapy.
                     Some of the most common injuries that have benefitted from PRP Therapy are:

                     <li>Osteoarthritis of the major joints (knee, shoulder, hip, and spine)</li>
                     <li>Rotator cuff strains and tears</li>
                     <li>Anterior Cruciate Ligament (ACL) injuries</li>
                     <li>Pelvic pain and instability</li>
                     <li>Back and neck injuries or pain</li>
                     <li>Tendonosis (Tennis Elbow)</li>
                     <li>Tendonitis and ligament sprains</li>
                     <li>Chronic knee pain (arthritis or injury)</li>
                     <li>Muscle strains</li>
                     Our plasma treatments strengthen these weak tissues, leading to improved joint and muscle functions with significantly less pain.
                  </p>


                    <p class="text_description" style="  color: #1B405A;">
                      <h5> The PRP Process.</h5>
                      <img style="float: left;" src="info/PRP process.jpg" alt="" class="img-fluid">
                      Depending on your particular injury, one to multiple sessions of platelet-rich 
                      plasma may be required. Each patient is unique, we work with you to create 
                      an ideal plan for you to provide the best chance of a successful outcome.
                   </p>

                   <p class="text_description" style="  color: #1B405A;">
                    <h5>Before your PRP session:</h5>
                  
                    It is helpful to remember that there are a few things to take into consideration before your session:
                   <li> At least three to four days before your procedure, discontinue anti-inflammatory medications (Celebrex, arcoxia, brufen for example)</li>
                   <li> One to two weeks before your procedure, discontinue any blood thinning herbs, supplements, or vitamins </li>
                   <li> One to two weeks before your procedure, discontinue systemic steroids</li>
                   <li> At least one month before your procedure, discontinue steroid injections</li>
                   <li> On the day of your procedure, eat a full, healthy breakfast and be sure to drink plenty of water</li>
                   
                    Please be advised that if you take chronic oral steroids, a discontinuation plan will need to be discussed with your primary care provider as abrupt stoppage may cause side effects. 
                    Please let us know if you are on any chronic medication so we can check if its safe to stop.
                 </p>

                 <p class="text_description" style="  color: #1B405A;">
                  <h5> After your PRP session:</h5>
                  After your PRP Therapy session, we recommend that all patients rest on the day of their procedure.
                   For the next 2 days, we advise limited use of the injection area, but movement is encouraged. 
                  This helps the joint recover as the injection is absorbed by the area’s surrounding tissues.
                </p>

                <p>
                  <h5> 0 – 3 days post-procedure guidelines.</h5>
                  After your treatment, there is a chance you’ll experience some mild to moderate pain or discomfort at the injection site. This is normal, and it is a sign that your body is healing as it adjusts to the injection. It is best to stay hydrated and drink plenty of water to help promote healing.
                  It is critical that you DO NOT take ANY medications following your procedure unless specifically directed otherwise by a physician. This is because any medication can affect the healing process or other adverse effects.
               </p>

                
                <p class="text_description" style="  color: #1B405A;">
                  <h5>Therefore, discontinue use of any of these medications post-procedure for best results:</h5>
                   <li>Anti-inflammatory medications for at least 2 weeks post-procedure</li>
                   <li>Blood-thinning herbs, supplements, or vitamins for 3-4 days post-procedure</li>
                   <li>Systematic steroids for 2 weeks post-procedure</li>
                </p>
              
                <p class="text_description" style="  color: #1B405A;">
                  <h5>Other important guidelines to follow after your PRP procedure are:</h5>
                   <li>Avoid applying ice or heat to the injection site for the first 72 hours post-procedure</li>
                   <li>Don’t take a very hot bath or go to a sauna for the first few days post-procedure</li>
                   <li>Avoid consumption of any alcoholic beverages for the first week post-procedure</li>
                   <li>Avoid showering for the first 24 hours following your procedure</li>
                   <li>Limit caffeine consumption for the first week post-procedure</li>
                   <li>Avoid smoking </li>
                </p>

                <p class="text_description" style="  color: #1B405A;">
                  <h5>3 – 14 days post-PRP</h5>
                  At this point in your healing, you should gradually increase your daily activities. This is also when you can begin exercises. Proper exercise is a vital part of long-term results, and we can help you determine which exercises are best suited for your needs prior to your procedure.
                <li>  Ice can be applied 3-4 times a day for 15-20 minutes as needed for comfort</li>
                 <li>
                  Anti-inflammatory medications (such as Tylenol) can be taken to help manage any residual pain at this time, take as directed
                  </li> 
                  <li> Continue to avoid alcoholic beverages, smoking, or excessive amounts of caffeine</li>
                 
                </p>
                
            
                <p class="text_description" style="  color: #1B405A;">
                  <h5> 3 – 4 weeks post-PRP:</h5>
                  At this point in your healing, we highly encourage you to begin physical therapy
                  to aid in your long-term healing and continued recovery. A follow-up appointment will be made 
                  to review your healing process. It is important that you keep this follow up visit even if your
                   recovery is going well. If there are any adjustments 
                 to your customized treatment plan, this is when we will be able to make those decisions.
                </p>
