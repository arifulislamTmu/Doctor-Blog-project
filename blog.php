                <h3  style="  color: #1B405A;">Hallux Valgus (bunions)</h3>
                <p class="text_description" style="  color: #1B405A;">
                  When the big toe begins leaning more and more outwards, 
                  crowding the smaller toes on your foot, it can cause many problems. The smaller
                  toes may begin crossing over or under the big toe, resulting in difficulty in shoe wear and
                  ulceration of the toes due to rubbing. You may also experience pain after longer walks or
                  often even at the start of the day, restricting your flexibility and mobility in your daily activities. 
                </p>
                <img src="info/Hallux Valgus 01.jpg" alt="" class="img-fluid">
                <p class="text_description" style="  color: #1B405A;">
                  I specialise in the gentle treatment of misalignment of the big toe with minimally 
                  invasive procedures on the forefoot. The goal of hallux valgus treatment being to help you
                  regain your mobility as quickly as possible and experience permanent medical and cosmetic 
                  improvement of your hallux valgus. The more severe the deformity, the more complex the surgery will be.
                  That is a reason to see a specialist, for gentle options to correct hallux valgus early.
                </p>
                <h4>What causes the pain?</h4>
                <p class="text_description" style="  color: #1B405A;">The bunion itself can become inflamed and push against the inside of the shoe. 
                  The smaller toes next to the big toe can become deformed, rubbing on shoes or catching on 
                  objects causing pain. The arch of the foot can become painful as the normal tripod balance within the
                   frame of the foot is disrupted. Lastly, the deformity, 
                  degenerative and progressive in nature can lead to arthritis, which can cause pain.</p>
                  
              <h4>When does hallux valgus need surgery?</h4>
                <img src="info/Hallux Valgus 02.jpg" style="float: left;" alt="" class="img-fluid">
                <p class="text_description" style="  color: #1B405A;">
                  Bunions cannot be corrected by changing shoes, using insoles or foot exercises. Although insoles
                  may provide relief for painful hallux valgus, they do not correct the misalignment.
                  Hallux valgus is often a cosmetic problem for many female patients - especially in open-toed shoes,
                  requiring correction. Another reason for surgery, is pain. Surgery normally includes the following 
                  procedures: An osteotomy (repositioning of the big toe in a straight position), removal of the offending 
                  bony bump, as well as tight soft tissue release and tendon correction, all via small, neat surgical incisions.
                  In addition to straightening the big toe, the goal of treating bunions surgically in early stages,
                  is to preserve the joints at risk of developing arthritis.
                  This allows us to  restore the balance of the foot and  the quality of life of our patients.
                </p>

                <h4  style="  color: #1B405A;">What is the success rate of bunion surgery?</h4>
                <p class="text_description" style="  color: #1B405A;">
                  The potential benefits and complications of bunion surgery must always be
                   weighed up carefully. In international studies on this issue, about 80% of patients 
                   felt very well following surgery. Another 10–15% felt better than before surgery, even 
                   if they still had minor problems. Only about
                   5% of all the patients in the studies experienced no improvement from bunion surgery. 
                </p>

                <h4>FAQ: Frequently asked questions about hallux valgus surgery</h4>
                <p class="text_description" style="  color: #1B405A;"><h6> How long does hallux valgus surgery take to heal? </h6>
                 Healing is complete after four to six
                   weeks. Incapacity for work, however, 
                  also depends on your job: A standing job requires more time off than a sitting job.
                </p>

                <p class="text_description" style="  color: #1B405A;"><h5>Can I walk after hallux valgus surgery? </h5>
                  Using a forefoot decompression shoe you can
                   walk immediately following surgery. 
                  This forefoot decompression shoe prevents the foot from rolling off across the big toe.
                </p>
                
                <p class="text_description" style="  color: #1B405A;"><h5>  Is the foot immobilised following surgery? </h5>
                    The foot is not put in a cast or 
                    immobilised following hallux surgery.
                    The big toe also remains passively flexible.
                </p>
                <p class="text_description" style="  color: #1B405A;"><h5> When can I return to wearing regular shoes? </h5>
                    Even after completing follow-up
                    treatment with the forefoot decompression shoe you should wear soft, 
                    comfortable and wide shoes for another four to six weeks.
                 </p>
